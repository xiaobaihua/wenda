package com.xbh.wenda.bean;

/**
 * @author xbh
 * @date
 * @Description
 */
public class User {
	private String user;
	private Integer age;
	private String sex;

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}
}
